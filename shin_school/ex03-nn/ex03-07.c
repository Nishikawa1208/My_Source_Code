#include <stdio.h>

int main(void)
{
    int p, y1000, y2000, y5000, y10000, i;

    printf("買い物の金額を入力してください："); scanf("%d", &p);

    i = p;
    y10000 = i / 10000;
    i -= y10000 * 10000;
    y5000 = i / 5000;
    i -= y5000 * 5000;
    y2000 = i / 2000;
    i -= y2000 * 2000;
    y1000 = (i + 999) / 1000;

    printf("必要な紙幣の枚数は、一万円札%d枚、五千円札%d枚、二千円札%d枚、千円札%d枚です。", y10000, y5000, y2000, y1000);

    return 0;
}